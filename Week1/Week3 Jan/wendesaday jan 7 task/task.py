# write a python function merge_dictionary that takes two dictionaries as input and returns a new dictionary that
# merge the two, if there are overlapping keys, the values in the second dictionary should
# overwrite the value in tht dictionary
# Dict1 = {'a': 1,'b': 2,'c': 3,
# Dict2 = {'b': 4, 'd': 5}

def merge_dicts(dict1, dict2):
    merged_dict = {}
    for key, item in dict1.items():
        merged_dict[key]= item
    for key, item in dict2.items():
        merged_dict[key] = item
    return merged_dict
dict1 = {'a': 1,'b': 2,'c': 3}
dict2 = {'b': 4, 'd': 5}

result = merge_dicts(dict1,dict2)
print(result)




































































































































































































